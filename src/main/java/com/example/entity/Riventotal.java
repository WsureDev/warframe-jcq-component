package com.example.entity;

public class Riventotal {
    private Integer qq;

    private Integer maxdaily;

    private Integer maxhave;

    private Integer rollcount;

    public Integer getQq() {
        return qq;
    }

    public void setQq(Integer qq) {
        this.qq = qq;
    }

    public Integer getMaxdaily() {
        return maxdaily;
    }

    public void setMaxdaily(Integer maxdaily) {
        this.maxdaily = maxdaily;
    }

    public Integer getMaxhave() {
        return maxhave;
    }

    public void setMaxhave(Integer maxhave) {
        this.maxhave = maxhave;
    }

    public Integer getRollcount() {
        return rollcount;
    }

    public void setRollcount(Integer rollcount) {
        this.rollcount = rollcount;
    }
}